public class OnLineBean {
    private String kay;
    private String value;

    public OnLineBean(String kay, String value) {
        this.kay = kay;
        this.value = value;
    }

    public String getKay() {
        return kay;
    }

    public void setKay(String kay) {
        this.kay = kay;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
